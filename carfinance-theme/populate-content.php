function cf_create_or_update_page($slug, $title, $content, $template = '', $parent_id = 0) {
    $existing = get_page_by_path($slug);

    if ($existing) {
        // Update existing page
        $result = wp_update_post([
            'ID'           => $existing->ID,
            'post_title'   => $title,
            'post_content' => $content,
            'post_status'  => 'publish',
            'post_parent'  => $parent_id,
        ]);
        if ($template) {
            update_post_meta($existing->ID, '_wp_page_template', $template);
        }
        echo "UPDATED page: {$slug} (ID: {$existing->ID})\n";
        return $existing->ID;
    } else {
        // Create new page
        $page_id = wp_insert_post([
            'post_title'   => $title,
            'post_name'    => $slug,
            'post_content' => $content,
            'post_status'  => 'publish',
            'post_type'    => 'page',
            'post_parent'  => $parent_id,
        ]);
        if ($template && !is_wp_error($page_id)) {
            update_post_meta($page_id, '_wp_page_template', $template);
        }
        echo "CREATED page: {$slug} (ID: {$page_id})\n";
        return $page_id;
    }
}

function cf_create_post($post_type, $title, $content, $meta = [], $tax = []) {
    // Check for duplicates by title
    $existing = get_posts([
        'post_type'   => $post_type,
        'title'       => $title,
        'post_status' => 'publish',
        'numberposts' => 1,
    ]);

    if ($existing) {
        echo "SKIP (exists): [{$post_type}] {$title}\n";
        return $existing[0]->ID;
    }

    $post_id = wp_insert_post([
        'post_title'   => $title,
        'post_content' => $content,
        'post_status'  => 'publish',
        'post_type'    => $post_type,
    ]);

    if (is_wp_error($post_id)) {
        echo "ERROR: [{$post_type}] {$title}: " . $post_id->get_error_message() . "\n";
        return 0;
    }

    foreach ($meta as $key => $value) {
        update_post_meta($post_id, $key, $value);
    }

    foreach ($tax as $taxonomy => $terms) {
        wp_set_object_terms($post_id, $terms, $taxonomy);
    }

    echo "CREATED [{$post_type}]: {$title} (ID: {$post_id})\n";
    return $post_id;
}

function cf_ensure_term($taxonomy, $name, $slug = '', $parent = 0) {
    $term = term_exists($name, $taxonomy);
    if ($term) return (int) $term['term_id'];

    $args = [];
    if ($slug) $args['slug'] = $slug;
    if ($parent) $args['parent'] = $parent;

    $result = wp_insert_term($name, $taxonomy, $args);
    if (is_wp_error($result)) {
        echo "TERM ERROR: {$taxonomy}/{$name}: " . $result->get_error_message() . "\n";
        return 0;
    }
    echo "TERM CREATED: {$taxonomy}/{$name}\n";
    return (int) $result['term_id'];
}


